#!/bin/bash
# Clone bluesky-social/social-app at a pinned commit and wrap its
# metro.config.js so TRANSFORMER=swc switches to react-native-swc. Idempotent.
#
# Bluesky is a real-world Expo + RN + Reanimated app — exercises the worklets
# plugin and a much larger module graph than examples/expo. Not vendored;
# clone target is gitignored at examples/bluesky/.

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
BLUESKY_DIR="$REPO_ROOT/examples/bluesky"
BLUESKY_REPO="https://github.com/bluesky-social/social-app.git"
# Tag 1.121.0 — Expo SDK 54, RN 0.81.5, Reanimated 3.19.1, Metro 0.83.3.
# Matches examples/expo's stack so the worklets plugin can plug in cleanly.
BLUESKY_REF="1.121.0"

command -v git >/dev/null  || { echo "git is required" >&2; exit 1; }
command -v yarn >/dev/null || { echo "yarn (classic) is required for bluesky's lockfile" >&2; exit 1; }

# 1. Clone (shallow) at the pinned ref, or fast-forward an existing clone.
if [ ! -d "$BLUESKY_DIR/.git" ]; then
    echo "==> Cloning bluesky-social/social-app@$BLUESKY_REF"
    git clone --depth 1 --branch "$BLUESKY_REF" "$BLUESKY_REPO" "$BLUESKY_DIR"
else
    current_ref="$(git -C "$BLUESKY_DIR" describe --tags --exact-match 2>/dev/null || echo unknown)"
    if [ "$current_ref" != "$BLUESKY_REF" ]; then
        echo "==> Updating bluesky checkout to $BLUESKY_REF"
        git -C "$BLUESKY_DIR" fetch --depth 1 origin "tag" "$BLUESKY_REF"
        git -C "$BLUESKY_DIR" checkout "tags/$BLUESKY_REF"
    else
        echo "==> bluesky already at $BLUESKY_REF"
    fi
fi

# 2. Wrap their metro.config.js. We don't patch in-place — we move the
# original aside (idempotent) and replace metro.config.js with a small wrapper
# that requires it and conditionally applies withSwcTransformer.
ORIGINAL_CFG="$BLUESKY_DIR/metro.config.original.js"
WRAPPER_CFG="$BLUESKY_DIR/metro.config.js"

if [ ! -f "$ORIGINAL_CFG" ]; then
    echo "==> Backing up upstream metro.config.js to metro.config.original.js"
    mv "$WRAPPER_CFG" "$ORIGINAL_CFG"
fi

echo "==> Writing lingui macro stub"
cat > "$BLUESKY_DIR/metro.lingui-stub.js" <<'STUB'
// Bluesky uses @lingui/react/macro and @lingui/babel-plugin-lingui-macro,
// expanded at compile time by babel-plugin-macros. Our SWC pipeline doesn't
// run babel, so the Metro resolver redirects every `@lingui/*/macro` request
// here. Re-export the runtime API where one exists, no-op otherwise. Bundle
// output diverges from babel mode (untranslated call sites), but the work
// the transformer does on each file remains representative.
try {
    module.exports = require('@lingui/react');
} catch {
    module.exports = {};
}
STUB

echo "==> Writing react-native-swc wrapper to metro.config.js"
cat > "$WRAPPER_CFG" <<'WRAPPER'
// Auto-generated by benchmarks/setup-bluesky.sh.
// Wraps bluesky's upstream metro config (preserved as metro.config.original.js)
// so TRANSFORMER=swc opts into react-native-swc with the worklets plugin.
const path = require('path');

const RN_SWC_DIR = path.resolve(__dirname, '../../packages/react-native-swc');
const WORKLETS_WASM = path.resolve(
  __dirname,
  '../../packages/worklets-plugin/worklets.wasm',
);
const { withSwcTransformer } = require(RN_SWC_DIR);

// Sentry's serializer (sentryMetroSerializer.js) does
// `countLines = require('metro/private/lib/countLines')` and calls
// `countLines(code)` directly — but Metro 0.83 ships countLines as
// `{__esModule: true, default: fn}`, breaking the call. Pre-warm the cache
// with a patched module before bluesky's config requires sentry.
(function patchSentryCountLines() {
  try {
    const resolved = require.resolve('metro/private/lib/countLines');
    const original = require(resolved);
    require.cache[resolved].exports = original.default || original;
  } catch (_) {}
})();

const cfg = require('./metro.config.original.js');

// Bluesky's babel config relies on babel-plugin-module-resolver to map
// `#/foo` → `<root>/src/foo`. SWC pipeline doesn't run babel, so resolve the
// alias at Metro's resolver layer instead. Composes with any existing
// resolveRequest that bluesky may add.
//
// Same pattern handles `@lingui/*/macro` and the loose lingui-macro side-
// effect import — those would normally be expanded by babel-plugin-macros,
// which doesn't run in our SWC pipeline. Redirect every macro-side request
// to the stub above so resolution succeeds and the bundle completes.
const aliasRoot = path.resolve(__dirname, 'src');
const linguiStub = path.resolve(__dirname, 'metro.lingui-stub.js');
const prevResolveRequest = cfg.resolver && cfg.resolver.resolveRequest;
cfg.resolver = {
  ...cfg.resolver,
  resolveRequest: (context, moduleName, platform) => {
    if (moduleName === '#' || moduleName.startsWith('#/')) {
      return context.resolveRequest(
        context,
        aliasRoot + moduleName.slice(1),
        platform,
      );
    }
    if (
      moduleName.startsWith('@lingui/') &&
      (moduleName.endsWith('/macro') || moduleName.includes('/macro/'))
    ) {
      return { type: 'sourceFile', filePath: linguiStub };
    }
    return prevResolveRequest
      ? prevResolveRequest(context, moduleName, platform)
      : context.resolveRequest(context, moduleName, platform);
  },
};

// SWC's WASM plugin loader resolves bare plugin specifiers via process.cwd().
// Bluesky's node_modules doesn't have @react-native-swc/worklets-plugin, so
// pass the absolute path to the .wasm artifact instead.
module.exports =
  process.env.TRANSFORMER === 'swc'
    ? withSwcTransformer(cfg, { plugins: [[WORKLETS_WASM, {}]] })
    : cfg;
WRAPPER

# 3. Make sure react-native-dotenv has something to read.
[ -f "$BLUESKY_DIR/.env" ] || : > "$BLUESKY_DIR/.env"

# 4. Install deps. yarn install only — no native build (we only bundle JS).
# Run from inside bluesky/ so corepack picks up bluesky's `yarn@1.22.22`
# pin (our repo root pins pnpm, which would otherwise win the upward walk).
# Skip if node_modules already populated — yarn install on bluesky is ~20m.
if [ -d "$BLUESKY_DIR/node_modules/expo" ]; then
    echo "==> Skipping yarn install (node_modules/expo present)"
else
    echo "==> yarn install (this takes a while)"
    ( cd "$BLUESKY_DIR" && \
        COREPACK_ENABLE_DOWNLOAD_PROMPT=0 \
        yarn install --frozen-lockfile --ignore-scripts )
fi

echo
echo "Setup complete. Run benchmarks/bundle.sh bluesky next."
